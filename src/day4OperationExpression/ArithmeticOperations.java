package day4OperationExpression;

public class ArithmeticOperations {
    // operation is what action a program can carry out on values stored on a variables
    // 1 Arithmetic operation - applied on numbers
    // + , -, /, *, %

    public static void main(String[] args) {

        double a = 1.5;
        int b = 3;
        /*
        int sum  = a + b;
        int substract = a - b;
        double division = a/b;
        int reminder = a % b;
        int multiply = a * b;
      */
        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a / b = " + (a / b));
        System.out.println("a % b = " + (a % b));
        System.out.println("a * b = " + (a * b));
    }

}
